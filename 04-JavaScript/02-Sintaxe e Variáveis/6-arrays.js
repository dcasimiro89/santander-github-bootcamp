let array = [];

array.push(3); // adicionando o número no final do array
array.push(2);
array.pop(); // retirar o último numero do array
array.shift() // retira o primeiro número do indice
array.unshift(1); // adiciona o número no primeiro indice
array.includes(3); // verifica se tem o número 3 na lista
array.reverse(); // inverte a lista
